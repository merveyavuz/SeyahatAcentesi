    private void jButton_guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_guncelleActionPerformed

        String eposta = txt_mail.getText();
        String tel = txt_tel.getText();

        if (txt_mail.getText().compareTo("") == 0 || txt_tel.getText().compareTo("") == 0) {
            JOptionPane.showMessageDialog(rootPane, "GÜNCELLENECEK VERİ GİRİNİZ.", "UYARI...", HEIGHT);
        } else {

            try {

                Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/SeyahatAcentesiDB", "sa", "as");
                String sorgu = "SELECT * FROM MUSTERI WHERE TC=" + id;

                Statement stmt = con.createStatement();

                ResultSet rs = stmt.executeQuery(sorgu);

                if (rs.next()) {
                    stmt.executeUpdate("UPDATE MUSTERI SET EMAIL='" + eposta + "', TELEFON='" + tel + "' WHERE TC=" + id + "");
                    JOptionPane.showMessageDialog(rootPane, "KAYIT BAŞARILI.", "", HEIGHT);
                }

            } catch (SQLException ex) {
                Logger.getLogger(GirisYap.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jButton_guncelleActionPerformed

